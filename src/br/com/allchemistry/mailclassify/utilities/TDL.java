    public static String extractDomain(String host) {
        int lastIndex = host.length() - 1;
        int beginIndex = 0;
        while (beginIndex < lastIndex) {
            int endIndex = host.indexOf('.', beginIndex);
            if (endIndex == -1) {
                break;
            } else {
                String tdl = host.substring(endIndex);
                if (tdlMap.contains(tdl)) {
                    return host.substring(beginIndex);
                }
                beginIndex = endIndex + 1;
            }
        }
        beginIndex = host.lastIndexOf('.') + 1;
        return host.substring(beginIndex);
    }
    
